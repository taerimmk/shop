package com.june.shop.admin.web;

import java.util.Collection;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.SessionAttributes;
import org.springframework.web.servlet.ModelAndView;

import com.june.shop.admin.model.Owner;
import com.june.shop.admin.service.SampleAdminService;

/**
 * @author Juergen Hoeller
 * @author Ken Krebs
 * @author Arjen Poutsma
 * @author Michael Isvy
 */
@Controller
@SessionAttributes(types = Owner.class)
public class SampleAdminController {
	
	@Autowired
    private SampleAdminService sampleService;


    /**
     * Custom handler for displaying an owner.
     *
     * @param ownerId the ID of the owner to display
     * @return a ModelMap with the model attributes for the view
     */
    @RequestMapping("/admin/sample/list")
    public ModelAndView sampleListadmin() {
        ModelAndView mav = new ModelAndView("admin/sample/sampleList");
        Collection<Owner> list = sampleService.sampleList();
        //StringUtils.
        mav.addObject("list", list);
        return mav;
    }

}
